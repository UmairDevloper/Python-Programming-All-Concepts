# The python program automatically prints a newline [\n] at the end of each print statement. 
#  To avoid this, we can use the end parameter [end=''] to print a string at the end of the statement.


# Without using end parameter 
str1 = 'Hello World'
print(str1)

str2 = 'Dear Sir!. '
str3 = 'How are you...'

# With using end parameter 

print(str2, end='')
print(str3)